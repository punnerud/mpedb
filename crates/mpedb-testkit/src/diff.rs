const SQLITE3: &str = "/usr/bin/sqlite3";

const MPEDB_SCHEMA: &str = r#"
[[table]]
name = "t"
primary_key = ["pk"]

  [[table.column]]
  name = "pk"
  type = "int64"

  [[table.column]]
  name = "a"
  type = "int64"

  [[table.column]]
  name = "b"
  type = "float64"

  [[table.column]]
  name = "c"
  type = "text"
"#;

const SQLITE_SCHEMA: &str = "CREATE TABLE t (pk INTEGER PRIMARY KEY, a INT, b REAL, c TEXT) STRICT;";

const PG_SCHEMA: &str =
    "CREATE TABLE t (pk bigint PRIMARY KEY, a bigint, b double precision, c text);";

/// One generated statement. `is_select` decides whether row output is
/// compared (SELECTs) or only the success/failure status (DML).
#[derive(Clone, Debug)]
pub struct GenStmt {
    pub sql: String,
    pub is_select: bool,
    /// The column types this SELECT returns, in order. sqlite3 and psql both
    /// hand back untyped pipe-separated TEXT, so the shape is what tells the
    /// parser whether `650` is the integer 650 or the text "650" — mpedb
    /// returns typed values and would otherwise compare unequal to both.
    /// Empty for non-SELECTs.
    pub shape: Vec<Kind>,
}

/// The type of one output column, for parsing the text engines' rows back.
#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub enum Kind {
    Int,
    Float,
    Text,
}

/// The shape of the base probe `SELECT pk, a, b, c FROM t`.
const BASE_SHAPE: [Kind; 4] = [Kind::Int, Kind::Int, Kind::Float, Kind::Text];

/// Aggregate results of a differential run.
#[derive(Debug, Default, Clone, Copy)]
pub struct DiffStats {
    pub programs: usize,
    pub statements: usize,
    pub selects_compared: usize,
    /// Statements that failed in BOTH engines (constraint violations the
    /// generator provokes deliberately; counted to show the corpus actually
    /// exercises error paths).
    pub agreed_failures: usize,
}

/// Result of a three-way run: either it ran, or the environment cannot run
/// PostgreSQL (no root here; e.g. `initdb` missing). Callers should treat
/// [`PgDiff::Unavailable`] as a LOUD skip, never as a pass.
#[derive(Debug)]
pub enum PgDiff {
    Ran(DiffStats),
    Unavailable(String),
}

/// Run `programs` random programs of `stmts_per_program` statements each,
/// starting from `base_seed` (program k uses seed `base_seed + k`), against
/// mpedb and sqlite3. Returns stats, or a [`Failure`] carrying the seed and
/// the **minimized** diverging program.
pub fn run_differential(
    base_seed: u64,
    programs: usize,
    stmts_per_program: usize,
) -> Result<DiffStats> {
    run_differential_impl(base_seed, programs, stmts_per_program, None)
}

/// Three-way differential: mpedb vs sqlite3 vs PostgreSQL 16, one throwaway
/// cluster for the whole run (each program still gets a fresh `t`). Every
/// statement must agree in all three engines. Fails soft when the
/// environment cannot start PostgreSQL (see [`PgDiff::Unavailable`]).
pub fn run_differential_3way(
    base_seed: u64,
    programs: usize,
    stmts_per_program: usize,
) -> Result<PgDiff> {
    let cluster = match PgCluster::start() {
        Ok(c) => c,
        Err(Failure(msg)) if msg.starts_with(PG_UNAVAILABLE) => {
            return Ok(PgDiff::Unavailable(msg));
        }
        Err(e) => return Err(e),
    };
    run_differential_impl(base_seed, programs, stmts_per_program, Some(&cluster))
        .map(PgDiff::Ran)
}

fn run_differential_impl(
    base_seed: u64,
    programs: usize,
    stmts_per_program: usize,
    pg: Option<&PgCluster>,
) -> Result<DiffStats> {
    if !Path::new(SQLITE3).exists() {
        return Err(Failure(format!("{SQLITE3} not found")));
    }
    let mut stats = DiffStats::default();
    for k in 0..programs {
        let seed = base_seed + k as u64;
        let program = generate_program(seed, stmts_per_program);
        match check_program(&program, pg)? {
            Agreement::Agree {
                statements,
                selects,
                agreed_failures,
            } => {
                stats.programs += 1;
                stats.statements += statements;
                stats.selects_compared += selects;
                stats.agreed_failures += agreed_failures;
            }
            Agreement::Diverge(first_msg) => {
                let minimal = minimize(&program, pg)?;
                let detail = match check_program(&minimal, pg)? {
                    Agreement::Diverge(m) => m,
                    // Non-deterministic environment failure; report the
                    // original divergence.
                    Agreement::Agree { .. } => first_msg,
                };
                let engines = if pg.is_some() {
                    "mpedb/sqlite3/postgres"
                } else {
                    "mpedb/sqlite3"
                };
                let mut report = String::new();
                let _ = writeln!(
                    report,
                    "DIVERGENCE between {engines} (seed {seed}, program {k})"
                );
                let _ = writeln!(report, "{detail}");
                let _ = writeln!(
                    report,
                    "minimal program ({} statements):",
                    minimal.len()
                );
                for s in &minimal {
                    let _ = writeln!(report, "  {};", s.sql);
                }
                return Err(Failure(report));
            }
        }
    }
    Ok(stats)
}

// ---------------------------------------------------------------- outcomes

/// Per-statement outcome, normalized across engines. Row values for SELECTs
/// are kept engine-native and compared by [`values_equal`].
enum Outcome {
    Ok,
    /// SELECT rows (engine-native values; mpedb: `Value`, sqlite: text).
    Rows(Vec<Vec<Field>>),
    Failed,
}

/// A single normalized field.
#[derive(Debug, Clone, PartialEq)]
enum Field {
    Null,
    Int(i64),
    Float(f64),
    Text(String),
}

impl std::fmt::Display for Field {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Field::Null => f.write_str("NULL"),
            Field::Int(v) => write!(f, "{v}"),
            // %.3f both sides (module docs, difference #1)
            Field::Float(v) => write!(f, "{v:.3}"),
            Field::Text(s) => f.write_str(s),
        }
    }
}

/// Tolerant float comparison (difference #1 in the module docs). Generated
/// arithmetic is exact in binary, so this tolerance only absorbs the text
/// round-trip through sqlite's output formatting.
fn fields_equal(a: &Field, b: &Field) -> bool {
    match (a, b) {
        (Field::Float(x), Field::Float(y)) => {
            (x - y).abs() <= 1e-9 * x.abs().max(y.abs()).max(1.0)
        }
        _ => a == b,
    }
}

enum Agreement {
    Agree {
        statements: usize,
        selects: usize,
        agreed_failures: usize,
    },
    Diverge(String),
}

/// Run one program against mpedb, sqlite3 and (when `pg` is given)
/// PostgreSQL, and compare per statement. mpedb is the comparison anchor:
/// every other engine is checked against it, which makes all engines
/// pairwise-equal on agreement.
fn check_program(program: &[GenStmt], pg: Option<&PgCluster>) -> Result<Agreement> {
    let mp = run_mpedb(program)?;
    let sq = run_sqlite(program)?;
    let pgv = match pg {
        Some(cluster) => Some(run_pg(program, cluster)?),
        None => None,
    };
    debug_assert_eq!(mp.len(), program.len());
    debug_assert_eq!(sq.len(), program.len());

    let mut selects = 0;
    let mut agreed_failures = 0;
    for (i, stmt) in program.iter().enumerate() {
        // (engine name, outcome) for every engine in this run.
        let mut outs: Vec<(&'static str, &Outcome)> =
            vec![("mpedb", &mp[i]), ("sqlite3", &sq[i])];
        if let Some(p) = &pgv {
            outs.push(("postgres", &p[i]));
        }

        // Success/failure status must be unanimous (difference #5: failures
        // compare only as "all failed", never by message).
        let n_failed = outs
            .iter()
            .filter(|(_, o)| matches!(o, Outcome::Failed))
            .count();
        if n_failed == outs.len() {
            agreed_failures += 1;
            continue;
        }
        if n_failed > 0 {
            let status = outs
                .iter()
                .map(|(name, o)| {
                    let s = if matches!(o, Outcome::Failed) {
                        "FAILED"
                    } else {
                        "ok"
                    };
                    format!("{name}={s}")
                })
                .collect::<Vec<_>>()
                .join(", ");
            return Ok(Agreement::Diverge(format!(
                "statement {i} status differs: {status}\n  sql: {}",
                stmt.sql
            )));
        }

        // All succeeded: SELECT row output must match mpedb's everywhere.
        match &mp[i] {
            Outcome::Rows(m) => {
                selects += 1;
                for (name, o) in &outs[1..] {
                    let Outcome::Rows(theirs) = o else {
                        return Ok(Agreement::Diverge(format!(
                            "statement {i}: result shape differs (mpedb rows vs {name} non-rows)\n  sql: {}",
                            stmt.sql
                        )));
                    };
                    if let Some(msg) = compare_rows(m, theirs, name) {
                        return Ok(Agreement::Diverge(format!(
                            "statement {i} SELECT output differs (mpedb vs {name})\n  sql: {}\n{msg}",
                            stmt.sql
                        )));
                    }
                }
            }
            Outcome::Ok => {
                if let Some((name, _)) = outs[1..]
                    .iter()
                    .find(|(_, o)| !matches!(o, Outcome::Ok))
                {
                    return Ok(Agreement::Diverge(format!(
                        "statement {i}: result shape differs (mpedb ok vs {name} rows)\n  sql: {}",
                        stmt.sql
                    )));
                }
            }
            Outcome::Failed => unreachable!("n_failed == 0 checked above"),
        }
    }
    Ok(Agreement::Agree {
        statements: program.len(),
        selects,
        agreed_failures,
    })
}

fn compare_rows(m: &[Vec<Field>], s: &[Vec<Field>], other_name: &str) -> Option<String> {
    let render = |rows: &[Vec<Field>]| -> String {
        rows.iter()
            .map(|r| {
                r.iter()
                    .map(|f| f.to_string())
                    .collect::<Vec<_>>()
                    .join("|")
            })
            .collect::<Vec<_>>()
            .join("\n    ")
    };
    let equal = m.len() == s.len()
        && m.iter()
            .zip(s)
            .all(|(rm, rs)| {
                rm.len() == rs.len() && rm.iter().zip(rs).all(|(a, b)| fields_equal(a, b))
            });
    if equal {
        None
    } else {
        Some(format!(
            "  mpedb rows ({}):\n    {}\n  {other_name} rows ({}):\n    {}",
            m.len(),
            render(m),
            s.len(),
            render(s)
        ))
    }
}

// ---------------------------------------------------------------- mpedb side

fn run_mpedb(program: &[GenStmt]) -> Result<Vec<Outcome>> {
    let dir = TempDir::new("diff")?;
    let toml = format!(
        "[database]\npath = \"{}\"\nsize_mb = 16\nmax_readers = 8\n{}",
        dir.db_path("diff").display(),
        MPEDB_SCHEMA
    );
    let config = Config::from_toml_str(&toml)
        .map_err(|e| Failure(format!("differential schema rejected: {e}")))?;
    let db = Database::open_with_config(config)
        .map_err(|e| Failure(format!("mpedb open failed: {e}")))?;

    let mut out = Vec::with_capacity(program.len());
    for stmt in program {
        out.push(match db.query(&stmt.sql, &[]) {
            Ok(ExecResult::Rows { rows, .. }) => {
                let mut frows = Vec::with_capacity(rows.len());
                for row in rows {
                    frows.push(
                        row.into_iter()
                            .map(|v| match v {
                                Value::Null => Field::Null,
                                Value::Int(x) => Field::Int(x),
                                Value::Float(x) => Field::Float(x),
                                Value::Text(s) => Field::Text(s),
                                other => Field::Text(format!("<unexpected {other:?}>")),
                            })
                            .collect(),
                    );
                }
                Outcome::Rows(frows)
            }
            Ok(_) => Outcome::Ok,
            Err(_) => Outcome::Failed,
        });
    }
    db.verify()
        .map_err(|e| Failure(format!("mpedb verify failed after program: {e}")))?;
    Ok(out)
}

// ---------------------------------------------------------------- sqlite side

/// Run the program in one sqlite3 batch process. Each statement occupies
/// exactly one script line, bracketed by `SELECT '@S<i>';` markers, so
/// stdout sections map to statements and stderr's `... near line N:` errors
/// map back through the line number.
fn run_sqlite(program: &[GenStmt]) -> Result<Vec<Outcome>> {
    let dir = TempDir::new("sqlite")?;
    let db_path = dir.path().join("diff.sqlite3");

    let mut script = String::new();
    let mut line_of_stmt: HashMap<usize, usize> = HashMap::new(); // line -> stmt idx
    let mut lineno = 0usize;
    let mut push_line = |script: &mut String, l: &str| {
        script.push_str(l);
        script.push('\n');
        lineno += 1;
        lineno
    };
    push_line(&mut script, ".mode list");
    push_line(&mut script, ".nullvalue NULL");
    push_line(&mut script, SQLITE_SCHEMA);
    for (i, stmt) in program.iter().enumerate() {
        push_line(&mut script, &format!("SELECT '@S{i}';"));
        debug_assert!(!stmt.sql.contains('\n'), "one line per statement");
        let ln = push_line(&mut script, &format!("{};", stmt.sql));
        line_of_stmt.insert(ln, i);
    }
    push_line(&mut script, "SELECT '@SEND';");

    let mut child = Command::new(SQLITE3)
        .arg(&db_path)
        .stdin(Stdio::piped())
        .stdout(Stdio::piped())
        .stderr(Stdio::piped())
        .spawn()
        .map_err(|e| Failure(format!("failed to spawn {SQLITE3}: {e}")))?;
    child
        .stdin
        .as_mut()
        .expect("piped stdin")
        .write_all(script.as_bytes())?;
    let output = child.wait_with_output()?;
    let stdout = String::from_utf8_lossy(&output.stdout);
    let stderr = String::from_utf8_lossy(&output.stderr);

    // stderr: `Runtime error near line N: ...` / `Parse error near line N: ...`
    let mut failed = vec![false; program.len()];
    for line in stderr.lines() {
        let Some(rest) = line.split("near line ").nth(1) else {
            if line.trim().is_empty() {
                continue;
            }
            return Err(Failure(format!(
                "unrecognized sqlite3 stderr line: {line}\nscript:\n{script}"
            )));
        };
        let n: usize = rest
            .split(':')
            .next()
            .and_then(|s| s.trim().parse().ok())
            .ok_or_else(|| Failure(format!("cannot parse sqlite3 error line: {line}")))?;
        let idx = *line_of_stmt.get(&n).ok_or_else(|| {
            Failure(format!(
                "sqlite3 error on unexpected script line {n}: {line}"
            ))
        })?;
        failed[idx] = true;
    }

    // stdout: marker-delimited sections.
    let mut sections: Vec<Vec<&str>> = vec![Vec::new(); program.len()];
    let mut current: Option<usize> = None;
    let mut saw_end = false;
    for line in stdout.lines() {
        if let Some(idx) = line.strip_prefix("@S") {
            if idx == "END" {
                saw_end = true;
                current = None;
                continue;
            }
            if let Ok(i) = idx.parse::<usize>() {
                if i < program.len() {
                    current = Some(i);
                    continue;
                }
            }
        }
        if let Some(i) = current {
            sections[i].push(line);
        }
        // Output before the first marker (CREATE TABLE) would be a harness
        // bug; there is none, so silently ignoring is fine.
    }
    if !saw_end {
        return Err(Failure(format!(
            "sqlite3 output truncated (no @SEND marker)\nstderr: {stderr}"
        )));
    }

    let mut out = Vec::with_capacity(program.len());
    for (i, stmt) in program.iter().enumerate() {
        if failed[i] {
            out.push(Outcome::Failed);
        } else if stmt.is_select {
            let mut rows = Vec::with_capacity(sections[i].len());
            for line in &sections[i] {
                rows.push(parse_pipe_row(line, &stmt.shape)?);
            }
            out.push(Outcome::Rows(rows));
        } else {
            out.push(Outcome::Ok);
        }
    }
    Ok(out)
}

// ------------------------------------------------------------- postgres side

/// Run the program in one `psql` batch against the throwaway cluster.
///
/// Statement status uses psql's `:ERROR` variable (true iff the last SQL
/// statement failed): every statement is followed by
/// `\if :ERROR \echo @E<i> \else \echo @K<i> \endif`, so each statement
/// deterministically emits exactly one status marker — sturdier than
/// inferring failure from a *missing* marker, and it never needs stderr
/// (which psql emits without usable line mapping). `ON_ERROR_STOP` stays
/// off so a failed statement — an *expected* event, the generator provokes
/// constraint violations constantly — rolls back alone (autocommit
/// statement-level rollback, difference #5) and the batch continues.
///
/// stdout layout per statement: `@S<i>` (via `\echo`), then the SELECT's
/// rows if any (tuples-only, `|`-separated, `NULL` sentinel — the exact
/// shape [`parse_pipe_row`] handles for sqlite), then `@K<i>` or `@E<i>`.
/// Marker lines cannot collide with row lines: rows of the fixed 4-column
/// SELECT always contain `|`, and generated text is lowercase-only.
fn run_pg(program: &[GenStmt], cluster: &PgCluster) -> Result<Vec<Outcome>> {
    let mut script = String::new();
    script.push_str("DROP TABLE IF EXISTS t;\n");
    script.push_str(PG_SCHEMA);
    script.push('\n');
    script.push_str("\\if :ERROR\n\\echo @SETUPFAIL\n\\endif\n");
    for (i, stmt) in program.iter().enumerate() {
        debug_assert!(!stmt.sql.contains('\n'), "one line per statement");
        let _ = writeln!(
            script,
            "\\echo @S{i}\n{};\n\\if :ERROR\n\\echo @E{i}\n\\else\n\\echo @K{i}\n\\endif",
            stmt.sql
        );
    }
    script.push_str("\\echo @SEND\n");

    let mut child = cluster
        .psql()
        .stdin(Stdio::piped())
        .stdout(Stdio::piped())
        .stderr(Stdio::piped())
        .spawn()
        .map_err(|e| Failure(format!("{PG_UNAVAILABLE}failed to spawn psql: {e}")))?;
    child
        .stdin
        .as_mut()
        .expect("piped stdin")
        .write_all(script.as_bytes())?;
    let output = child.wait_with_output()?;
    let stdout = String::from_utf8_lossy(&output.stdout);
    let stderr = String::from_utf8_lossy(&output.stderr);
    if !output.status.success() {
        return Err(Failure(format!(
            "psql exited with {}\nstderr: {stderr}",
            output.status
        )));
    }

    // stderr is deliberately not parsed: expected errors are detected via
    // :ERROR markers, and NOTICEs (e.g. from DROP TABLE IF EXISTS) are noise.
    let mut failed = vec![false; program.len()];
    let mut sections: Vec<Vec<&str>> = vec![Vec::new(); program.len()];
    let mut status_seen = vec![false; program.len()];
    let mut current: Option<usize> = None;
    let mut saw_end = false;
    for line in stdout.lines() {
        if line == "@SETUPFAIL" {
            return Err(Failure(format!(
                "postgres CREATE TABLE failed\nstderr: {stderr}"
            )));
        }
        if line == "@SEND" {
            saw_end = true;
            current = None;
            continue;
        }
        let marker = |prefix: &str| -> Option<usize> {
            line.strip_prefix(prefix)?
                .parse::<usize>()
                .ok()
                .filter(|&i| i < program.len())
        };
        if let Some(i) = marker("@S") {
            current = Some(i);
            continue;
        }
        if let Some(i) = marker("@K") {
            status_seen[i] = true;
            current = None;
            continue;
        }
        if let Some(i) = marker("@E") {
            failed[i] = true;
            status_seen[i] = true;
            current = None;
            continue;
        }
        if let Some(i) = current {
            sections[i].push(line);
        }
    }
    if !saw_end || status_seen.iter().any(|&s| !s) {
        return Err(Failure(format!(
            "psql output truncated (missing @SEND or a status marker)\n\
             stdout:\n{stdout}\nstderr: {stderr}"
        )));
    }

    let mut out = Vec::with_capacity(program.len());
    for (i, stmt) in program.iter().enumerate() {
        if failed[i] {
            out.push(Outcome::Failed);
        } else if stmt.is_select {
            let mut rows = Vec::with_capacity(sections[i].len());
            for line in &sections[i] {
                rows.push(parse_pipe_row(line, &stmt.shape)?);
            }
            out.push(Outcome::Rows(rows));
        } else {
            out.push(Outcome::Ok);
        }
    }
    Ok(out)
}

/// Parse one pipe-separated output row against the statement's declared
/// `shape` — the shared wire format of sqlite3's `.mode list` and psql's
/// unaligned tuples-only mode. Generated text never contains `|` or the
/// literal string `NULL`, so the split and the NULL sentinel are unambiguous
/// (an empty text field is `''`, distinct from the sentinel — difference
/// #11).
///
/// The shape is per-statement rather than fixed because aggregates return
/// whatever they return: `count(*)` is an integer where the column under it
/// is text, and `avg(a)` is a float where `a` is an integer.
fn parse_pipe_row(line: &str, shape: &[Kind]) -> Result<Vec<Field>> {
    let parts: Vec<&str> = line.split('|').collect();
    if parts.len() != shape.len() {
        return Err(Failure(format!(
            "row has {} fields, expected {}: {line}",
            parts.len(),
            shape.len()
        )));
    }
    let mut out = Vec::with_capacity(shape.len());
    for (part, kind) in parts.iter().zip(shape) {
        out.push(match (kind, *part) {
            (_, "NULL") => Field::Null,
            (Kind::Int, s) => s
                .parse::<i64>()
                .map(Field::Int)
                .map_err(|_| Failure(format!("cannot parse int field `{s}` in row: {line}")))?,
            // PG returns `sum(bigint)` and `avg(bigint)` as NUMERIC, printed
            // with a decimal point and full scale ("162.5000000000000000");
            // sqlite and mpedb use f64. Parsing both as f64 and comparing with
            // difference #1's tolerance is what makes those agree.
            (Kind::Float, s) => s
                .parse::<f64>()
                .map(Field::Float)
                .map_err(|_| Failure(format!("cannot parse float field `{s}` in row: {line}")))?,
            (Kind::Text, s) => Field::Text(s.to_string()),
        });
    }
    Ok(out)
}

// ---------------------------------------------------------------- minimizer

/// Greedy delta-minimization: repeatedly try dropping one statement; keep
/// the removal if the program still diverges (in the same engine set that
/// found it). Quadratic in program length, but only runs on failure.
fn minimize(program: &[GenStmt], pg: Option<&PgCluster>) -> Result<Vec<GenStmt>> {
    let mut best: Vec<GenStmt> = program.to_vec();
    loop {
        let mut shrunk = false;
        let mut i = 0;
        while i < best.len() {
            let mut candidate = best.clone();
            candidate.remove(i);
            if candidate.is_empty() {
                break;
            }
            if matches!(check_program(&candidate, pg)?, Agreement::Diverge(_)) {
                best = candidate;
                shrunk = true;
                // re-test the same index (now the next statement)
            } else {
                i += 1;
            }
        }
        if !shrunk {
            return Ok(best);
        }
    }
}

