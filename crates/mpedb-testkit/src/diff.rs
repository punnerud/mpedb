//! Randomized differential tester: the same generated SQL program runs
//! against **mpedb**, against **/usr/bin/sqlite3** (a STRICT table, whose
//! rigid typing matches mpedb's), and — in the three-way mode — against a
//! throwaway **PostgreSQL 16** cluster ([`crate::pg::PgCluster`]).
//! Per-statement outcomes plus every SELECT's output are compared across
//! all engines. Any divergence is minimized (greedy statement removal,
//! re-checking every engine each step) and reported as a self-contained
//! reproduction.
//!
//! # Fixed schema
//!
//! mpedb (TOML):  `t(pk int64 PRIMARY KEY, a int64, b float64, c text)`
//! sqlite:        `CREATE TABLE t (pk INTEGER PRIMARY KEY, a INT, b REAL,
//!                 c TEXT) STRICT;`
//! postgres:      `CREATE TABLE t (pk bigint PRIMARY KEY, a bigint,
//!                 b double precision, c text);`
//!
//! # Known semantic differences — normalized or avoided in generation
//!
//! Each of these would produce false-positive divergences; the generator or
//! comparator handles them explicitly:
//!
//! 1. **Float formatting.** sqlite prints REAL with `%!.15g`-style text;
//!    mpedb returns f64 directly. The comparator parses sqlite's text and
//!    compares numerically with tolerance (and generated float literals are
//!    multiples of 0.25, exactly representable, so arithmetic stays exact).
//! 2. **Text collation.** mpedb compares text bytewise (binary collation);
//!    sqlite's default BINARY collation matches — but only because the
//!    generator emits pure ASCII (lowercase letters). Case differences would
//!    also hit sqlite's ASCII-case-insensitive `LIKE`, so all generated text
//!    and LIKE patterns are lowercase-only.
//! 3. **`INTEGER PRIMARY KEY` rowid aliasing.** sqlite treats `pk` as the
//!    rowid. Same lookup/uniqueness semantics for this schema, EXCEPT that
//!    inserting NULL into it auto-assigns a rowid where mpedb raises
//!    NOT NULL — so the generator never writes NULL (or omits) `pk`.
//! 4. **Division/modulo.** sqlite yields NULL on division by zero; mpedb
//!    raises an error (and PostgreSQL raises a different error). `/` and
//!    `%` are never generated.
//! 5. **Constraint errors.** PK/UNIQUE/NOT-NULL failures compare only as
//!    "all engines failed", never by message. Every engine rolls back the
//!    whole failed statement (sqlite: statement-level ABORT; mpedb:
//!    autocommit transaction abort; postgres: statement-level rollback in
//!    autocommit), so state stays in sync.
//! 6. **Integer overflow.** mpedb errors, sqlite wraps to REAL, postgres
//!    errors. Generated literals and self-referential updates keep |values|
//!    far below i64 range, so overflow is unreachable.
//!
//! # PostgreSQL-specific differences (three-way mode)
//!
//! 7. **Float output text.** `double precision` prints shortest-exact
//!    decimal (`extra_float_digits=1` default since PG 12), and an integer
//!    assigned into `b` prints without a decimal point (`10`, not `10.0`).
//!    Already absorbed by difference #1's numeric comparison — psql rows
//!    are parsed with the same `parse_pipe_row` as sqlite's.
//! 8. **`ORDER BY` NULLS placement.** PostgreSQL defaults to NULLS LAST in
//!    ASC (and NULLS FIRST in DESC); mpedb and sqlite do the opposite. Moot
//!    here by construction: the generator only ever orders by `pk`, which
//!    is NOT NULL — pinned by the `generator_only_orders_by_pk` unit test
//!    so a generator change cannot silently re-open this hole.
//! 9. **No STRICT needed.** PostgreSQL is rigidly typed already — closer to
//!    mpedb than sqlite is. The plain `CREATE TABLE` above needs no
//!    equivalent of sqlite's `STRICT` marker; type-correct-by-construction
//!    generation (see [`gen_pred`]) keeps the remaining coercion corners
//!    (e.g. `bigint` → `double precision` on `SET b = a`) identical.
//! 10. **LIKE case sensitivity.** PostgreSQL's `LIKE` is case-sensitive,
//!     exactly like mpedb's. The lowercase-only rule from difference #2
//!     stays purely for sqlite's ASCII-case-insensitive `LIKE`.
//! 11. **Empty string vs NULL.** PostgreSQL keeps `''` and NULL distinct,
//!     like mpedb (and sqlite; Oracle is the odd one out). The generator
//!     deliberately emits `''` text literals (values and predicate
//!     comparands) so this is *exercised*, not just assumed — pinned by the
//!     `generator_covers_empty_string` unit test. In psql output the empty
//!     field is unambiguous because NULL prints as the `NULL` sentinel
//!     (`-P null=NULL`, same convention as sqlite's `.nullvalue NULL`).
//!
//! Determinism: programs are generated by a seeded xorshift
//! ([`crate::Xorshift`]); a failure report includes the seed, and the
//! minimized program reproduces it standalone.

